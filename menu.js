function mostrar() {
    document.getElementById("sidebar").style.width = "13%";
	document.getElementById("sidebar").style.height = "100%";
    document.getElementById("abrir").style.display = "none";
	document.getElementById("cerrar").style.display = "block";
}
function ocultar() {
    document.getElementById("sidebar").style.width = "0";
    document.getElementById("abrir").style.display = "inline";
	document.getElementById("cerrar").style.display = "none";
}
function abrir1(){
    document.getElementById("abrir1").style.display="none";
    document.getElementById("cerrar1").style.display="block";
    document.getElementById("ol1").style.display="block";
    document.getElementById("ol1").style.height="auto";
}
function cerrar1(){
    document.getElementById("abrir1").style.display="block";
    document.getElementById("cerrar1").style.display="none";
    document.getElementById("ol1").style.display="none";
    document.getElementById("ol1").style.height="0";
}
